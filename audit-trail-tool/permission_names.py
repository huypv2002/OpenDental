AUDIT_DISPLAY_OVERRIDES = {
    2: "FamilyModuleViewed",
    3: "AccountModuleViewed",
    4: "TreatmentPlanModuleViewed",
    5: "ChartModuleViewed",
    6: "ImagesModuleViewed",
}


def permission_name(value) -> str:
    try:
        code = int(str(value).strip())
    except (TypeError, ValueError):
        return str(value or "")
    if code in AUDIT_DISPLAY_OVERRIDES:
        return AUDIT_DISPLAY_OVERRIDES[code]
    if 0 <= code < len(PERMISSION_NAMES):
        return PERMISSION_NAMES[code]
    return f"Unknown ({code})"
